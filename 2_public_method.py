# 字符串拼接
result = "hello" + "world"
print(result)

# 列表合并
result = [1,2,] + [3,4]
print(result)
result = (5,6) + (7,8)
print(result)

# 复制
result = "A" * 5
print(result)

result = [12, 36] * 3
print(result)

result = ("星星", "月亮") * 4
print(result)

# in 判断参数是否在指定对象里面
result = "a" in "add"
print(result)
